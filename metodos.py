#METODOS DE LAS CADENAS:
#UPPER
print("Hola Chicos UNAHUR".upper())

#LOWER 
print("Hola CHICOS UNAHUR".lower())

#CAPITALIZE
print("Hola CHICOS UNAHUR".capitalize())

#TITLE
print("Hola CHICOS UNAHUR".title())

#COUNT
print("Hola CHICOS UNAHUR".count("CHICOS"))

#FIND
print("Hola CHICOS UNAHUR".find("Hola"))

#ISDIGIT
x = "100"
print(x.isdigit())


#ISALNUM

c= "1fdg23"
print(c.isalnum())

#ISALPHA
v = "hvgh"
print(v.isalpha())


#ISLOWER

print("hola".islower())

#ISUPPER

print("HOLA".isupper())

#ISTITLE

print("Hola che".istitle())

#ISSPACE
print("  ---  ".isspace())



#SPLIT
print("Hola,como,estas,vos".split(","))

#JOIN 
print(",".join("Buenas Noches America"))

#STRIP
print("   aca antes habia muchos espacios     ".strip())

#REPLACE
print("Hola Chicos Como estan".replace("o", "0"))